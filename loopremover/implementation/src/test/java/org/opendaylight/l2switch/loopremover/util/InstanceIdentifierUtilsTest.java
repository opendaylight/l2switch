package org.opendaylight.l2switch.loopremover.util;

import static org.junit.jupiter.api.Assertions.assertEquals;

import org.junit.jupiter.api.Test;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.NodeConnectorId;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.NodeId;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.Nodes;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.node.NodeConnector;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.node.NodeConnectorKey;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.nodes.Node;
import org.opendaylight.yang.gen.v1.urn.opendaylight.inventory.rev130819.nodes.NodeKey;
import org.opendaylight.yang.gen.v1.urn.tbd.params.xml.ns.yang.network.topology.rev131021.NetworkTopology;
import org.opendaylight.yang.gen.v1.urn.tbd.params.xml.ns.yang.network.topology.rev131021.TopologyId;
import org.opendaylight.yang.gen.v1.urn.tbd.params.xml.ns.yang.network.topology.rev131021.network.topology.Topology;
import org.opendaylight.yang.gen.v1.urn.tbd.params.xml.ns.yang.network.topology.rev131021.network.topology.TopologyKey;
import org.opendaylight.yangtools.yang.binding.InstanceIdentifier;

class InstanceIdentifierUtilsTest {
    @Test
    void testCreateNodeConnectorIdentifier() {
        assertEquals(InstanceIdentifier.builder(Nodes.class)
            .child(Node.class, new NodeKey(new NodeId("nodeId")))
            .child(NodeConnector.class, new NodeConnectorKey(new NodeConnectorId("ncId")))
            .build(), InstanceIdentifierUtils.createNodeConnectorIdentifier("nodeId", "ncId"));
    }

    @Test
    void testGenerateTopologyInstanceIdentifier() {
        assertEquals(InstanceIdentifier.builder(NetworkTopology.class)
            .child(Topology.class, new TopologyKey(new TopologyId("topo")))
            .build(), InstanceIdentifierUtils.generateTopologyInstanceIdentifier("topo"));
    }
}
